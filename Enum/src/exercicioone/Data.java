public class Data
{
    private int dia;
    private int month;

    public Data()
    {
    }

    public Data(int dia, int mes)
    {
        this.dia = dia;
        this.month = mes;
    }

    public int getDia()
    {
        return dia;
    }

    public int getMonth()
    {
        return month;
    }
    
    
}
